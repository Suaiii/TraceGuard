"""Generate the combined case-evidence figure (#15-A) by vertically stitching
the three individual case plates (stable / degraded / conflict).

The individual plates are generated by plot_case_evidence.py with identical
academic-premium styling.  This script stacks them on a white canvas with
consistent inter-card gaps, then renders the single footnote at the bottom.

Design spec:
- White canvas background
- Three cards stacked top-to-bottom: stable → degraded → conflict
- Equal vertical gap between adjacent grey cards
- Gap between bottom card and white canvas bottom edge for the footnote
- Footnote (#6C757D, italic, 8.5pt) floats on the white canvas, not inside any card
"""

import argparse
from pathlib import Path

from PIL import Image, ImageDraw, ImageFont

# ── Constants ─────────────────────────────────────────────────
ROLES = ["stable", "degraded", "conflict"]

FOOTNOTE_TEXT = (
    "数据来源：case_summary.csv / case_classification/all.csv（真实系统输出）。"
    "可疑区域计数与篡改类型为工程定位证据，不等同于像素级真值标注。"
    "像素级定位指标见 experiments/localization/verified_results/。"
)


# ── Font discovery ────────────────────────────────────────────
def _find_chinese_font(size: int) -> ImageFont.FreeTypeFont:
    """Return a PIL ImageFont that supports Chinese, falling back to default."""
    candidates = [
        "C:/Windows/Fonts/msyh.ttc",       # Microsoft YaHei
        "C:/Windows/Fonts/simhei.ttf",     # SimHei
        "C:/Windows/Fonts/msyhbd.ttc",     # Microsoft YaHei Bold
        "/usr/share/fonts/truetype/droid/DroidSansFallbackFull.ttf",
        "/usr/share/fonts/opentype/noto/NotoSansCJK-Regular.ttc",
    ]
    for path in candidates:
        if Path(path).exists():
            try:
                return ImageFont.truetype(path, size)
            except Exception:
                continue
    return ImageFont.load_default()


# ── Helpers ───────────────────────────────────────────────────
def _crop_white_borders(im: Image.Image, margin: int = 2) -> Image.Image:
    """Crop white/transparent borders from a PIL RGBA image."""
    import numpy as np
    arr = np.array(im)
    # Treat white (>=250) and fully transparent as background
    mask = ~(
        ((arr[:, :, 0] >= 250) & (arr[:, :, 1] >= 250) & (arr[:, :, 2] >= 250))
        | (arr[:, :, 3] == 0)
    )
    rows = np.any(mask, axis=1)
    cols = np.any(mask, axis=0)
    if not rows.any() or not cols.any():
        return im
    rmin, rmax = np.where(rows)[0][[0, -1]]
    cmin, cmax = np.where(cols)[0][[0, -1]]
    rmin = max(0, rmin - margin)
    rmax = min(im.height, rmax + margin)
    cmin = max(0, cmin - margin)
    cmax = min(im.width, cmax + margin)
    return im.crop((cmin, rmin, cmax, rmax))


# ── Main stitching ────────────────────────────────────────────
def generate_combined_figure(
    stable_path,
    degraded_path,
    conflict_path,
    output_base,
    inter_card_gap=3,
    footnote_bottom_margin=15,
):
    """Stack three pre-rendered case plates on a white canvas with a footnote.

    Parameters
    ----------
    stable_path : str or Path
        PNG path for the stable case plate.
    degraded_path : str or Path
        PNG path for the degraded case plate.
    conflict_path : str or Path
        PNG path for the conflict case plate.
    output_base : str or Path
        Output path without extension (PNG/SVG/PDF).
    inter_card_gap : int
        Vertical pixel gap between adjacent grey cards (at 300 DPI).
    footnote_bottom_margin : int
        Pixels from canvas bottom to footnote baseline.
    """
    paths = {
        "stable": Path(stable_path),
        "degraded": Path(degraded_path),
        "conflict": Path(conflict_path),
    }

    # Load all three plates and crop white borders
    plates = {}
    for role in ROLES:
        p = paths[role]
        if not p.exists():
            raise FileNotFoundError(f"Missing plate: {p}")
        im = Image.open(p).convert("RGBA")
        plates[role] = _crop_white_borders(im)

    # Ensure consistent width — pad narrower images to match the widest
    max_w = max(im.width for im in plates.values())

    def _pad_to_width(im: Image.Image, target_w: int) -> Image.Image:
        if im.width == target_w:
            return im
        padded = Image.new("RGBA", (target_w, im.height), (255, 255, 255, 255))
        offset_x = (target_w - im.width) // 2
        padded.paste(im, (offset_x, 0), im)
        return padded

    for role in ROLES:
        plates[role] = _pad_to_width(plates[role], max_w)

    # ── Canvas layout ───────────────────────────────────────
    # Footnote area
    footnote_font = _find_chinese_font(26)  # ~12pt at 300 DPI
    # Measure footnote text height
    temp_im = Image.new("RGBA", (1, 1))
    temp_draw = ImageDraw.Draw(temp_im)
    bbox = temp_draw.textbbox((0, 0), FOOTNOTE_TEXT, font=footnote_font)
    footnote_text_h = bbox[3] - bbox[1]
    footnote_area = footnote_text_h + footnote_bottom_margin * 2  # top + bottom margin

    canvas_w = max_w
    canvas_h = (
        plates["stable"].height
        + inter_card_gap
        + plates["degraded"].height
        + inter_card_gap
        + plates["conflict"].height
        + inter_card_gap  # gap between bottom card and footnote
        + footnote_area
    )

    canvas = Image.new("RGBA", (canvas_w, canvas_h), (255, 255, 255, 255))
    draw = ImageDraw.Draw(canvas)

    # ── Paste plates ────────────────────────────────────────
    y_cursor = 0
    for role in ROLES:
        canvas.paste(plates[role], (0, y_cursor), plates[role])
        y_cursor += plates[role].height + inter_card_gap

    # ── Footnote on white canvas ─────────────────────────────
    footnote_y = canvas_h - footnote_bottom_margin - footnote_text_h // 2
    draw.text(
        (canvas_w // 2, footnote_y),
        FOOTNOTE_TEXT,
        fill=(108, 117, 125),  # #6C757D
        font=footnote_font,
        anchor="ms",  # middle-center
    )

    # ── Save ─────────────────────────────────────────────────
    output_base = Path(output_base)
    output_base.parent.mkdir(parents=True, exist_ok=True)
    saved = []

    # PNG (300 DPI)
    png_path = output_base.with_suffix(".png")
    canvas.convert("RGB").save(png_path, dpi=(300, 300))
    saved.append(png_path)

    # SVG / PDF — render the stitched canvas via matplotlib for vector output
    _save_vector(canvas, output_base, saved)

    return saved


def _save_vector(canvas: Image.Image, output_base: Path, saved: list):
    """Render the stitched canvas to SVG and PDF via matplotlib."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    dpi = 300
    w_in = canvas.width / dpi
    h_in = canvas.height / dpi

    fig, ax = plt.subplots(figsize=(w_in, h_in), dpi=dpi)
    ax.imshow(canvas.convert("RGB"))
    ax.set_xticks([])
    ax.set_yticks([])
    for spine in ax.spines.values():
        spine.set_visible(False)
    fig.subplots_adjust(0, 0, 1, 1)

    for ext, kw in (("svg", {}), ("pdf", {})):
        p = output_base.with_suffix(f".{ext}")
        fig.savefig(p, facecolor="white", dpi=dpi, **kw)
        if ext == "svg":
            svg = p.read_text(encoding="utf-8")
            p.write_text(
                "\n".join(line.rstrip() for line in svg.splitlines()) + "\n",
                encoding="utf-8",
            )
        saved.append(p)

    plt.close(fig)


# ── CLI ───────────────────────────────────────────────────────
def main(argv=None):
    parser = argparse.ArgumentParser(
        description="Stitch three case plates into a combined figure with footnote"
    )
    parser.add_argument(
        "--stable", default="docs/figures/socialmedia/socialmedia_case_stable.png",
        help="Path to stable case plate PNG"
    )
    parser.add_argument(
        "--degraded", default="docs/figures/socialmedia/socialmedia_case_degraded.png",
        help="Path to degraded case plate PNG"
    )
    parser.add_argument(
        "--conflict", default="docs/figures/socialmedia/socialmedia_case_conflict.png",
        help="Path to conflict case plate PNG"
    )
    parser.add_argument(
        "--output", required=True,
        help="Output path without extension"
    )
    parser.add_argument(
        "--gap", type=int, default=3,
        help="Vertical gap between cards in pixels at 300 DPI (default: 3)"
    )
    args = parser.parse_args(argv)

    for path in generate_combined_figure(
        args.stable, args.degraded, args.conflict, args.output,
        inter_card_gap=args.gap,
    ):
        print(path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
